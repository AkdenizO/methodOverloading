public class VierRechenarten {
    public int addition(int zahl1, int zahl2){
        return zahl1 + zahl2;
    }

    public int subraktion(int zahl1, int zahl2){
        return zahl1 - zahl2;
    }

    public int multiplikation(int zahl1, int zahl2){
        return zahl1 * zahl2;
    }

    public double division(int zahl1, int zahl2){
        return zahl1 / zahl2;
    }
    public int addition(int zahl1, int zahl2, int zahl3){
        return zahl1 + zahl2 + zahl3;
    }
}
